void EXTI4_IRQHandler(void)
{
	if (EXTI -> PR & EXTI_PR_PR4)
		{
		EXTI -> PR = EXTI_PR_PR4;
		BB(GPIOB -> ODR, PB13) = 1;
		}
}

void EXTI3_IRQHandler(void)
{
	if (EXTI -> PR & EXTI_PR_PR5)
	{
		EXTI -> PR = EXTI_PR_PR5;
		BB(GPIOB -> ODR, PB13) = 0;
	}
}

